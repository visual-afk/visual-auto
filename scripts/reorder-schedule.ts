const rows = await fetchAllRows();

// 주제별로 행 묶기 (각 주제는 정확히 2행)
const topicRows = new Map<string, typeof rows>();
for (const r of rows) {
  if (!topicRows.has(r.topic)) topicRows.set(r.topic, []);
  topicRows.get(r.topic)!.push(r);
}

let updated = 0;
for (const [topic, dates] of newSchedule) {
  const matchedRows = topicRows.get(topic);
  if (!matchedRows || matchedRows.length < 2) {
    console.log(`⚠️ "${topic}" — 시트에 행 없음 또는 부족`);
    continue;
  }
  // 행 2개를 날짜순으로 정렬
  matchedRows.sort((a, b) => a.rowIndex - b.rowIndex);

  // 첫번째 = 아임웹, 두번째 = 블로그
  await updateCell(matchedRows[0].rowIndex, SHEET_COLUMNS.SCHEDULED_DATE, dates[0]);
  await updateCell(matchedRows[1].rowIndex, SHEET_COLUMNS.SCHEDULED_DATE, dates[1]);

  console.log(`✅ ${topic}: ${dates[0]} (행 ${matchedRows[0].rowIndex}) + ${dates[1]} (행 ${matchedRows[1].rowIndex})`);
  updated += 2;
}

console.log(`\n총 ${updated}개 행 날짜 재배치 완료`);
